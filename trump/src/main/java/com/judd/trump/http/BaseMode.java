package com.judd.trump.http;


/**
 * @author 王元_Trump
 * @desc 请求
 * @time 2016/12/12 18:13
 */
public class BaseMode<T> {

    private int code;
    private String message;
    private T data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
